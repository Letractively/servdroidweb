package org.servDroid.util;

import android.util.Log;

public class Logger {
	
	public static final String TAG = "ServDroid";
	
	/** Use this flag to hide or show the log generated by this class*/
	public static boolean enableLog = true;
	
	private static StringBuffer buff = new StringBuffer();
	
	public static void d(String msg){
		if (enableLog){
			buff.append(msg+ "\n");
			Log.d(TAG, msg);
		}
	}
	
	public static void e(String msg){
		if (enableLog){
			buff.append(msg+ "\n");
			Log.e(TAG, msg);
		}
	}
	
	public static void w(String msg){
		if (enableLog){
			buff.append(msg+ "\n");
			Log.w(TAG, msg);
		}
	}
	
	public static void i(String msg){
		if (enableLog){
			buff.append(msg+ "\n");
			Log.i(TAG, msg);
		}
	}
	
	public static void clean(){
		buff.setLength(0);
	}
	public static String getLogString(){
		return buff.toString();
	}

}
